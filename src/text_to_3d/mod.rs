//!
//! AI-powered text/image to 3D mesh generation.
//!
//! This module converts 2D images into 3D triangle meshes using deep learning models.
//! Supports TripoSR (fast), InstantMesh (quality), and Wonder3D (multi-view).
//!
//! # Architecture
//!
//! The pipeline follows: Image -> Preprocess -> ONNX Inference -> Volume -> Marching Cubes -> Mesh
//!
//! # Model Research (2025-2026)
//!
//! - **TripoSR** by StabilityAI/Tripo: fastest single-image-to-3D, ~0.5s on GPU.
//!   Based on transformer architecture, outputs triplane features.
//!   ONNX export available via torch.onnx.export.
//! - **InstantMesh** by TencentARC: multi-view reconstruction with better topology.
//!   Uses sparse-view reconstruction, ~2s on GPU. ONNX partially supported.
//! - **Wonder3D** by xxlong0: multi-view diffusion, highest quality but ~10s.
//!   ONNX support experimental, kept as future integration stub.
//!
//! # Output Contract
//!
//! All mesh outputs use the `GeneratedMesh` struct with:
//! - Vertices: `[f32; 3]` positions in normalized [-1, 1] space
//! - Normals: `[f32; 3]` unit normals
//! - UVs: `[f32; 2]` texture coordinates in [0, 1]
//! - Triangles: `[u32; 3]` vertex index triplets
//!
//! # Quick Start
//!
//! ```rust,no_run
//! use xeno_lib::text_to_3d::{generate_3d, load_mesh_model, Mesh3DConfig};
//!
//! let config = Mesh3DConfig::default();
//! let mut session = load_mesh_model(&config)?;
//! let input = image::open("object.png")?;
//! let mesh = generate_3d(&input, &mut session)?;
//! println!("Generated {} vertices, {} triangles", mesh.vertices.len(), mesh.triangles.len());
//! mesh.export_obj("output.obj")?;
//! # Ok::<(), Box<dyn std::error::Error>>(())
//! ```

